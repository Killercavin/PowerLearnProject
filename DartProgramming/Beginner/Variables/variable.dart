// variable declaration in dart

void main(){
  // variable declaration
  
  /* In dart variables are declared using the 'var, dynamic and dataType' keyword. */
  var greeting = 'Hello'; // var keyword is used to declare a variable whose type is known at compile time.
  dynamic name = 'World'; // dynamic keyword is used to declare a variable whose type is not known at compile time.
  String message = 'Hello, World!'; // dataType is used to declare a variable whose type is known at compile time.
  int age = 20; // dataType is used to declare a variable whose type is known at compile time.
  bool isTrue = true; // dataType is used to declare a variable whose type is known at compile time.
  double pi = 3.14; // dataType is used to declare a variable whose type is known at compile time.

  print(greeting);
  print(name);
  print(message);
  print(age);
  print(isTrue);
  print(pi);
}